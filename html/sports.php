                <p>
                    KUALA LUMPUR: Presiden Persekutuan Bolasepak Antarabangsa (FIFA), Gianni Infantino percaya bola sepak Malaysia berada di landasan tepat untuk terus berkembang menerusi Peta Hala Tuju F:30 yang diperkenalkan Persatuan Bolasepak Malaysia (FAM) sejak 2018.
                    Infantino yang turut melancarkan F:30 di Kuala Lumpur pada 2018 turut mengakui Fasa 1 menunjukkan hasil yang memberangsangkan yang dizahirkan menerusi perkembangan dari segi prestasi skuad Harimau Malaya.
                    "Saya turut bersama-sama ketika melancarkan F:30 lima tahun lalu dan ia serta-merata menarik minat saya kerana ia begitu mendalam dan teliti.
                    "F:30 jelas menunjukkan bahawa tiada jalan pintas untuk kejayaan di peringkat antarabangsa. Daripada mensasarkan perubahan dalam satu malam, ia memerlukan pandangan jangka masa panjang dan fokus bagi membina asas urus tadbir yang baik, pertandingan dan juga infrastruktur.
                </p>
                <p>
                    "Tidak dapat dinafikan Fasa 1 F:30 adalah satu kejayaan dan ini dapat dilihat menerusi kejayaan Malaysia beraksi di pentas Piala Asia menerusi pusingan kelayakan selepas 40 tahun.
                    "Kita juga dapat menyaksikan Malaysia memperbaiki ranking FIFA secara konsisten," kata Infantino dalam klip video ketika pelancaran Fasa 2 F:30, minggu lalu.
                    Terdahulu, Fasa 2 F:30 dilancarkan bagi meneruskan kesinambungan Fasa 1 yang dilancarkan pada 2018 untuk mencapai sasaran mendominasi ASEAN dari segi urustadbir, pertandingan dan pembangunan.
                    Dalam masa sama, Infantino turut berbangga memandangkan program 'FIFA Forward' sememangnya memainkan peranan yang besar dalam pembangunan bola sepak Malaysia.
                    "Kita akan bekerjasama untuk membina padang dan lampu limpah untuk pusat latihan baharu di Putrajaya dan menerusi program 'FIFA Forward' juga, liga profesional wanita berjaya ditubuhkan.
                    "Kita akan melancarkan program bola sepak untuk sekolah yang bakal membawa memanfaatkan lebih tiga juta kanak-kanak," katanya.
                    Sebagai rekod, Program 'FIFA Forward' yang memasuki edisi ketiga dilancarkan pada Januari tahun ini bagi menyediakan dana yang lebih menyeluruh sebagai sokongan untuk projek pembangunan bola sepak di seluruh dunia.
